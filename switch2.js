const age=30;
switch (age) {
    case 18:
        console.log("You can vote")
        break;
    case 21:
        console.log("You can drink")
    case 25:
        console.log("You can drive")
    case 30:
        console.log("You can marry")
    case 60:
        console.log("You are old")
    default:
        console.log("Invalid Age")
        break;
}